import math

print("=== โปรแกรมคำนวณพื้นที่รูปทรงเรขาคณิตอัจฉริยะ ===")
print("1. คำนวณพื้นที่รูปสี่เหลี่ยมมุมฉาก")
print("2. คำนวณพื้นที่รูปสามเหลี่ยม")
print("3. คำนวณพื้นที่รูปวงกลม")

try:
    choice = int(input("กรุณาเลือกหมายเลขรูปทรงที่ต้องการคำนวณ (1-3): "))

    if choice == 1:
        กว้าง = float(input("กรอกความกว้าง (หน่วย): "))
        ยาว = float(input("กรอกความยาว (หน่วย): "))
        พื้นที่ = กว้าง * ยาว
        print(f"-> พื้นที่รูปสี่เหลี่ยมมุมฉากคือ: {พื้นที่:.2f} ตารางหน่วย")

    elif choice == 2:
        ฐาน = float(input("กรอกความยาวฐาน (หน่วย): "))
        สูง = float(input("กรอกความสูง (หน่วย): "))
        พื้นที่ = 0.5 * ฐาน * สูง
        print(f"-> พื้นที่รูปสามเหลี่ยมคือ: {พื้นที่:.2f} ตารางหน่วย")

    elif choice == 3:
        รัศมี = float(input("กรอกความยาวรัศมี r (หน่วย): "))
        พื้นที่ = math.pi * (รัศมี ** 2)
        print(f"-> พื้นที่รูปวงกลมคือ: {พื้นที่:.2f} ตารางหน่วย")
        
    else:
        print("ข้อผิดพลาด: กรุณาเลือกหมายเลข 1-3 เท่านั้น")

except ValueError:
    print("ข้อผิดพลาด: กรุณากรอกข้อมูลเป็นตัวเลขที่ถูกต้องเท่านั้น!")